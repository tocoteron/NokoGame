	public static int[][] createvalueOFPlace(int x, int y)
	{
		int xx, yy;

		if (x == 4 && y == 4)
		{
			return valueOFPlace44;
		}
		else if (x == 6 && y == 6)
		{
			return valueOFPlace66;
		}
		else if (x == 8 && y == 8)
		{
			return valueOFPlace88;
		}
		else if (x == 10 && y == 10)
		{
			return valueOFPlace1010;
		}
		else if (x == 12 && y == 12)
		{
			return valueOFPlace1212;
		}

		// fill all 10
		for (yy = 0; yy < y; yy++)
		{
			for (xx = 0; xx < x; xx++)
			{
				valueOFPlace[y][x] = 10;
			}
		}

		for (yy = 0; yy < y; yy++)
		{
			if (yy == 0 || yy == (y - 1))
			{
				for (xx = 0; xx < x; xx++)
				{
					valueOFPlace[y][x] = 50;
				}
			}
			else
			{
				valueOFPlace[y][0] = 50;
				valueOFPlace[y][x - 1] = 50;
			}
		}

		valueOFPlace[0][0] = 100;
		valueOFPlace[0][x - 1] = 100;
		valueOFPlace[y - 1][0] = 100;
		valueOFPlace[y - 1][x - 1] = 100;

		return valueOFPlace;
	}
